def send_msg(soc, msg: bytes) -> None:
    """
    Helper function for sending message to a socket object.

    First sends the message length so that the socket object knows how big
    the message it so that it can adjust the buffer size for receiving the response.
    """
    msg_len = str(len(msg)).ljust(HEADER_SIZE).encode()
    soc.send(msg_len)
    soc.send(msg)


if __name__ == "__main__":
    # Generate a key to encrypt transmission during the session.
    session_key = get_random_bytes(16)

    # Generate private and public key for the server
    priv_key, pub_key = utils.gen_rsa_keys()

    host_addr = socket.gethostbyname(socket.gethostname())
    server_soc = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    server_soc.bind((host_addr, PORT))

    server_soc.listen()
    print(f"[S] Listening on {host_addr} port {PORT}.")

    while True:
        client_soc, client_addr = server_soc.accept()
        print(f"[S] Connected to client on {host_addr}.")

        # Receive the client's public key
        client_pub_key_len = client_soc.recv(HEADER_SIZE).decode()
        client_pub_key = client_soc.recv(int(client_pub_key_len))

        # Send the encrypted session key to the client
        encrypted_session_key = utils.rsa_encrypt(client_pub_key, session_key)
        send_msg(client_soc, encrypted_session_key)

        connected = True
        while connected:
            iv_len = client_soc.recv(HEADER_SIZE).decode()
            if iv_len:
                iv = client_soc.recv(int(iv_len))

                # Receive the encrypted message from the client.
                cipher_msg_len = client_soc.recv(HEADER_SIZE).decode()
                cipher_msg = client_soc.recv(int(cipher_msg_len))

                # Decode the encrypted message received from the client.
                msg = utils.aes_decrypt(cipher_msg, session_key, iv).decode()

                if msg == DISCONNECT_MSG:
                    connected = False
                    print(f"[S] Disconnected from client on {host_addr}.")
                else:
                    # Encrypt and send the response back to the client
                    response = MAPPING.get(msg, "Invalid Country Code!")
                    print(f"[S] Received: {msg}. Sending {response}.")

                    response_iv, response_cipher = utils.aes_encrypt(
                        session_key, response.encode()
                    )
                    send_msg(client_soc, response_iv)
                    send_msg(client_soc, response_cipher)

        client_soc.close()
